-- 19. Monthly Revenue Trend 

select * from sales;

SELECT 
MONTHNAME(STR_TO_DATE(date,'%Y-%m-%d')) AS month_name,
SUM(`units sold` * `price per unit`) AS revenue
FROM sales
GROUP BY month_name
ORDER BY revenue DESC;

-- 20. Customers Who Purchased More Than 5 Units in Total

select `customer name`, sum(`units sold`) as total_units
from sales
group by `customer name`
