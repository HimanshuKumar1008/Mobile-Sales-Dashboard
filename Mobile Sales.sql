create database mobile;
use mobile;

select * from sales;

-- 1. What is the total revenue generated from all mobile sales?

select sum(`units sold`*`price per unit`) as total_revenue
from sales; 

-- 2. Which mobile brand sold the most units?

select brand, sum(`units sold`) as units
from sales
group by brand 
order by units desc;

-- 3. Top 5 Cities with Highest Sales Revenue

select * from sales;

select city, sum(`units sold`*`price per unit`) as total_revenue
from sales
group by city 
order by total_revenue
limit 5;

-- 4. Best Selling Mobile Model

select * from sales;

select `mobile model`, sum(`units sold`) as best_mobile
from sales
group by `mobile model`
order by best_mobile; 

-- 5. Average Customer Rating by Brand

select brand, avg(`customer ratings`) as avg_ratings
from sales
group by brand
order by avg_ratings; 

-- 6. Revenue Generated by Payment Method

select `payment method`,sum(`units sold`*`price per unit`) as total_revenue
from sales
group by `payment method`
order by total_revenue desc;

-- 7. Total Sales per Year

select year, sum(`units sold`*`price per unit`) as total_sales
from sales
group by year 
order by year;

-- 8. Average Price per Unit by Brand

select brand, avg(`price per unit`) as avg_price
from sales
group by brand
order by avg_price;

-- 9. Top 10 Customers Who Purchased the Most Unit

select * from sales;

select `customer name`,sum(`units sold`) as total_unit
from sales
group by `customer name`
order by total_unit desc
limit 10;   

-- 10. Daily Sales Revenue

select date,sum(`units sold` * `price per unit`) as revenue
from sales
group by date
order by date;

-- 11. City-wise Average Customer Age

select city, avg(`customer age`) as avg_age
from sales
group by city;

-- 12. Highest Revenue Generating Brand

select brand, sum(`units sold`*`price per unit`) as highest_revenue
from sales
group by brand
order by highest_revenue desc
limit 1;

